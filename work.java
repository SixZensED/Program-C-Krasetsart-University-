    private void calculateChange() {
        try {
            int money = Integer.parseInt(jTextField1.getText());
            int fiftyBaht = money / 50;
            money %= 50;
            int fiveBaht = money / 5;
            money %= 5;
            int twoBaht = money / 2;
            money %= 2;
            int oneBaht = money;

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            model.addRow(new Object[]{"50 Baht", fiftyBaht});
            model.addRow(new Object[]{"5 Baht", fiveBaht});
            model.addRow(new Object[]{"2 Baht", twoBaht});
            model.addRow(new Object[]{"1 Baht", oneBaht});
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number.");
        }
    }

